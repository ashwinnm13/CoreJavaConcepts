package Day2;

public class LogicalOperators {

	public static void main(String[] args) 
	{
		boolean x = true, y = false;
        System.out.println("--Logical Operators--");
        
        System.out.println("x && y: " + (x && y)); // AND logic -->Strictly true
        
        System.out.println("x || y: " + (x || y)); // OR logic -->Any one is true
        
        System.out.println("!x: " + (!x));         // NOT logic -->Opposite output



	}

}
