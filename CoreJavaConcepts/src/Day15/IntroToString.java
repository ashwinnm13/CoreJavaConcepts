package Day15;

public class IntroToString {

	public static void main(String[] args) 
	{
        //String Literal
		String name = "KL Rahul";
		
		//Use new keyword to declare String -> String object
		String city = new String("Banglore");
		
		System.out.println("Name:"+name);
		System.out.println("City: "+city);
		
		System.out.println("Welcome "+name+" to "+city);
 
	}

}
