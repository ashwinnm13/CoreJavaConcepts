//This class is to demonstrate Method Overloading
package Day6;

public class Addition
{
   public int add(int a , int b)
   {
	   return a+b;
   }
   
   public int add(int a , int b ,int c)
   {
	   return a+b+c;
   }
   
   public double add(double a , double b)
   {
	   return a+b;
   }
   
   public String add(String c , String d)
   {
	   return c+d;
   }
}
